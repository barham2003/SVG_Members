export default function getDefinition(lang: "eng" | "kur"): string {
  if (lang === "kur") {
    return `گرووپی خۆبەخشی سماڕت گرووپێکی خۆبەخشی قازانج نەویستی ناحوکمیە لە بەرواری 14/5/2020 کاتێك کۆڕۆنا تەنگی بە
     خەڵكی هەڵچنیبوو، کۆمەڵێك گەنج بڕیارمان دا دەستەوەستان نەوەستین 
    و هەڵساین بە دامەزراندنی گرووپی خۆبەخشی سماڕت لە پەیمانگای سماڕت.
    لەو کاتەوە تا ئێستا گرووپ بەردەوام لە گەشەکردن بووە و لە ئێستادا لە زیاتر لە دووسەد ئەندام پێكدێت 
    و سنووری کارکردنی لە شاری کەرکوك و هەندێك لە شارەکانی تری هەرێمی کوردستانە
    `;
  }

  return `Smart Volunteer Group is a non-profit non-governmental volunteer group. On 14/5/2020, when the coronavirus was plaguing people, a group of young people decided not to stop and established Smart Volunteer Group at Smart Institute.
  Since then, the group has continued to grow and now consists of more than two hundred members and works in Kirkuk and some other cities in the Kurdistan Region.`;
}
